package info.date_4_20_21;

public class ShiftingOperator {
    public static void main(String[] args) {
        System.out.println("Right shifting: " + (10>>2)); //Right shifting: 2
        System.out.println("Right shifting: " + (20>>2)); //Right shifting: 5
        System.out.println("Right shifting: " + (5>>10)); //Right shifting: 0
    }
}
