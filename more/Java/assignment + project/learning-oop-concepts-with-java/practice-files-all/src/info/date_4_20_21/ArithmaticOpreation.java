package info.date_4_20_21;

public class ArithmaticOpreation {
    public static void main(String[] args) {
        int x = 10;
        int z = 2;
        System.out.println("Summation = " + (x+z)); //Summation = 12
        System.out.println("Subtraction = " + (x-z)); //Subtraction = 8
        System.out.println("Multiplication = " + (x*z)); //Multiplication = 20
        System.out.println("Division = " + (x/z)); //Division = 5
        System.out.println("Modulus = " + ((++x)%z)); //Modulus = 1

    }
}
