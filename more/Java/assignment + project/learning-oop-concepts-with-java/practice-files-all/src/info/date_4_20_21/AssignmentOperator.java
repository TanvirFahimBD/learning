package info.date_4_20_21;

public class AssignmentOperator {
    public static void main(String[] args) {

        int a = 10;
        System.out.println(a+=5); //15
        System.out.println(a-=5); //10
        System.out.println(a*=5); //50
        System.out.println(a/=5); //10
        System.out.println(a%=7); //3
    }
}
