    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String email = txtEmail.getText();
        String password = txtPassword.getText();
        try{
        if (email.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Some fields are missing.");
        } else {
            JDBCManager manager = new JDBCManager();
            manager.initSqliteConnection("bg.db");
            String sql = "SELECT p.playerId, p.password FROM Player p WHERE p.email = ?;";
            ArrayList<HashMap<String, String>> al = manager.getQueryData(sql, email);
            if (password.equals(al.get(0).get("password"))) {
                setCurrentPlayer(Integer.parseInt(al.get(0).get("playerId")));
                PlayerHome ph = new PlayerHome();
                ph.setVisible(true);
                this.setVisible(false);
            } else {
                JOptionPane.showMessageDialog(this, "<Html>Invalid password.<br>Please try again.</html>");
            }
        }
        }catch(IndexOutOfBoundsException outOfBoundsException){
            JOptionPane.showMessageDialog(this, "Invalid credentials.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

